x , y = 10 , 20
print(x < y and x != y)#and operator checks if both expressions are true and only then returns true else false
print(x > y or x == y)#or operator returns true if either of the conditions are true if both the conditions are false then it returns false
print(not(x > y))#not operator reverses the result of expression if expression is true , after using not operator it returns false

