x , y = 55 , 66
# all comparison operators return true if condition is satisfied and false if not
print(x == y) # == operator checks if the two values are equal or not
print(x > y)# checks if first value is greater than second
print(x < y)#checks if the first value is less than the second
print(x != y)#returns true if the values are not equal to each other
print(x <= y)#checks of the fist value is less than or equal to second
print(x >= y)#checks if fist value is greater than or equal to second